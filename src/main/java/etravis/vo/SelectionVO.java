package etravis.vo;

public class SelectionVO {
    private int esno;
    private int ecno;
    private int classificationid;
    private int eano;
	public int getEsno() {
		return esno;
	}
	public void setEsno(int esno) {
		this.esno = esno;
	}
	public int getEcno() {
		return ecno;
	}
	public void setEcno(int ecno) {
		this.ecno = ecno;
	}
	public int getClassificationid() {
		return classificationid;
	}
	public void setClassificationid(int classificationid) {
		this.classificationid = classificationid;
	}
	public int getEano() {
		return eano;
	}
	public void setEano(int eano) {
		this.eano = eano;
	}
}
